package ucu.edu.ua.apps;

/**
 * Represents a Chamomile flower.
 * This is a specific type of flower with predefined type.
 */
public class Chamomile extends Flower {
  /** Creates a new Chamomile flower with CHAMOMILE type. */
  public Chamomile() {
    super();
    this.setFlowerType(FlowerType.CHAMOMILE);
  }
}
